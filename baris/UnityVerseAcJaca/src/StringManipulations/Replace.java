package StringManipulations;

public class Replace {
    public static void main(String[] args) {

        String str = "Su an ders dinliyoruz   d d      dd    s  hjg  g g bb Ä± o    j ";


        System.out.println(str.replace("d","XxX"));
        System.out.println(str.replace("s","XxX"));
        System.out.println(str.replace("S","XxX"));
        System.out.println(str.replace(".","XxX"));

// str degiskeninde atanmis olan stringin bosluklar disinda kalan butun harflerinin uzunlugunu bulunuz.



    }
}
