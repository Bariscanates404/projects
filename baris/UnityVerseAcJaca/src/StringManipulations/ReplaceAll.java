package StringManipulations;

public class ReplaceAll {
    public static void main(String[] args) {

        String str = "Su an ders dinliyoruz";

        System.out.println(str.replaceAll("ders","asdf"));
        System.out.println(str.replaceAll("SsS","asdf"));
        System.out.println(str.replaceAll("\\s",""));




        // str degiskeninde atanmis olan stringin bosluklar disinda kalan butun harflerinin uzunlugunu bulunuz.





    }
}
